public class Program {

    /**
     * Determines if two arrays have the same length.
     * 
     * @param a an array
     * @param b an array
     * @return true if a and b have the same length, false otherwise
     */
    public boolean equalLength(int[] a, int[] b) {
        return a.length == b.length;
    }

}
